fun main() {
    // Declare a mutable variable for fruit count and initialize it
    var fruitCount = 10

    // Print out the initial fruit count using string interpolation
    println("Initial fruit count: $fruitCount")

    // Declare an immutable variable for fridge capacity and initialize it
    val fridgeCapacity = 50

    // Print out the fridge capacity using string interpolation
    println("Fridge capacity: $fridgeCapacity")

    // Update the fruit count variable
    fruitCount += 5

    // Print out the updated fruit count
    println("Updated fruit count: $fruitCount")
}
